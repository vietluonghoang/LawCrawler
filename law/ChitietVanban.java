package law;

public class ChitietVanban {

	private String so;
	private String tieude;
	private String noidung;
	private String minhhoa;
	private String cha;
	private String ong;
	private String cu;
	
	public String getSo() {
		return so;
	}
	public void setSo(String so) {
		this.so = so.trim();
	}
	public String getTieude() {
		return tieude;
	}
	public void setTieude(String tieude) {
		this.tieude = tieude.trim();
	}
	public String getNoidung() {
		return noidung;
	}
	public void setNoidung(String noidung) {
		this.noidung = noidung.trim();
	}
	public String getMinhhoa() {
		return minhhoa;
	}
	public void setMinhhoa(String minhhoa) {
		this.minhhoa = minhhoa.trim();
	}
	public String getCha() {
		return cha;
	}
	public void setCha(String cha) {
		this.cha = cha.trim();
	}
	public String getOng() {
		return ong;
	}
	public void setOng(String ong) {
		this.ong = ong.trim();
	}
	public String getCu() {
		return cu;
	}
	public void setCu(String cu) {
		this.cu = cu.trim();
	}
}
